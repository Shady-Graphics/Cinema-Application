    public boolean checkAmountGiven(double amount) {
        if (amount < sale.myTicket.getTicketPrice()) {
            txtFldCashGiven.setForeground(Color.red);
            JOptionPane.showMessageDialog(this, odeon.rb.getString("ERROR HIGHER VALUE")+ "  " + amount);
            return false;
        } else {
            txtFldCashGiven.setForeground(Color.black);
            return true;
        }
    }

