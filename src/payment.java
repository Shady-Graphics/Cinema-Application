    private void btnCalculateChangeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculateChangeActionPerformed
        try {
            double amount = Double.parseDouble(txtFldCashGiven.getText());
            if (checkAmountGiven(amount)) {
                sale.myTicket.setAmountTender(amount);

                new ticket().setVisible(true);
                this.setVisible(false);
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, odeon.rb.getString("ERROR ENTER VALUE") + "\n" + e.getMessage());
        }
    }//GEN-LAST:event_btnCalculateChangeActionPerformed

